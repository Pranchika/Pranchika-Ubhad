package wrapper_class;

public class WrapperClassIntoPrimitiveDatatype {

	public static void main(String[] args) {
		Integer a=new Integer (579);
		int b=a.intValue();
		System.out.println(b);

		
		
		
		Integer x=new Integer(60);
		int y=x.intValue();
		System.out.println(y);
		
		
		
		Integer p=new Integer(56);
		int q=p.intValue();
		System.out.println(q);
	}

}
