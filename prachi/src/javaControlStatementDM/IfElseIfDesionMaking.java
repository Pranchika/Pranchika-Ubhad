package javaControlStatementDM;

public class IfElseIfDesionMaking {

	public static void main(String[] args) {
		int a=50;
		if(a>=100) {
			System.out.println("a greater than equals 100");
		}
		else if (a==50) {
			System.out.println("a equals 50");
		}
		else if (a<40) {
			System.out.println("a less than 40");
		}
		else {
			System.out.println("none of the above condition is true");
		}

	}

}
