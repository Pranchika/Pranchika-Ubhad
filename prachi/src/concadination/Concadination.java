package concadination;

import java.lang.reflect.Array;

public class Concadination {
	byte a;
	short b;
	int c;
	long d;
	float e;
	double f;
	boolean g;
	char h;
	String i;
	Array j;

		public static void main(String[] args) {
		Concadination dv=new Concadination();
			System.out.println("Default value of byte datatype is : "+dv.a);
			System.out.println("Default value of short datatype is : "+dv.b);
			System.out.println("Default value of int datatype is : "+dv.c);
			System.out.println("Default value of long datatype is : "+dv.d);
			System.out.println("Default value of float datatype is : "+dv.e);
			System.out.println("Default value of double datatype is : "+dv.f);
			System.out.println("Default value of boolean datatype is : "+dv.g);
			System.out.println("Default value of char datatype is : "+dv.h);
			System.out.println("Default value of String datatype is : "+dv.i);
			System.out.println("Default value of Array datatype is : "+dv.j);
			
		}
}
