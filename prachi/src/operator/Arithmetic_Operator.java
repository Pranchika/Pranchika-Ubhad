package operator;

public class Arithmetic_Operator {
	public static void main(String[] args) {
int a=6;
int b=4;
System.out.println(a+b);
System.out.println(a*b);
System.out.println(a-b);
System.out.println(a%b);
System.out.println(a/b);
System.out.println(a++);
System.out.println(a);
System.out.println(a--);
System.out.println(a);
	}
}
